Name = "Guvi python"
word = Name.split()[-1]
print(word)

# The split() method is used to split the string into a list of words. By default, split() splits the string using
# whitespace as a separator. So, name.split() returns ["Guvi", "python"]. Then, we can simply get the second element
# of the list using indexing (name.split()[1]) which is the word "python". Finally, we print the word using the
# print() function.
